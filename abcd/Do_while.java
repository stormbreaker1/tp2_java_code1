package abcd;

// This is the java program for do-while loop
public class Do_while {
	public static void main(String[] args)
	{
		int num1 = 1;
		
		do
		{
			System.out.println("Code Geass is the Best Anime....");
			num1++;
		}
		while(num1<10);
		
		System.out.println("End of the Program.......");
	}
}

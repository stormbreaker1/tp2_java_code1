package abcd;

public class Else_if2 {
	public static void main(String[] args)
	{
		// initializing x with 26
		int x = 26;
		
		// putting "if" condition to check if x is greater than 10 
		if(x>10)
		{
			System.out.println("Number is greater than 10");
		}
		
		// "else if" condition to check is x is greater than 18
		else if(x>18)
		{
			System.out.println("x is greater than 18");
		}
	}
}

package abcd;

public class If_condition {
	public static void main(String[] args)
	{
		// initializing a variable
		int x = 23;
		
		// putting "if" condition to check if x is greater than 10 
		if(x> 10)
		{
			System.out.println("x is greater than 10");
		}
		
		// "if" condition to check if x is greater than 17
		if(x>17)
		{
			System.out.println("x is greater than 17");
		}
	}
}

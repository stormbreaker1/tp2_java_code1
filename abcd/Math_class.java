package abcd;

public class Math_class {
	public static void main(String[] args)
	{
		// Using math function in java
		
		// For getting ceiling value
		System.out.println("Ceiling value of 34.6 " + Math.ceil(34.6));
		
		// for getting absolute value
		System.out.println(Math.abs(-24));
		
		// for getting maximum put of two values
		System.out.println(Math.max(34, 45));
		
		// for finding cosine of some value
		System.out.println(Math.cos(90));
		
		// for finding the value of some number raise with some power
		System.out.println(Math.pow(4, 2));
		
		// for getting the value of PI
		System.out.println(Math.PI);
		
		
		// System.out.println(Math.));
	}
}
